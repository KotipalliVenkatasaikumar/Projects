package strings;

public class ArthmaticOperations {
	public int add(int a, int b) {
		int c= a+b;
		return c;
		}

	
	public int sub(int a, int b) {
		int c= a-b;
		return c;
	}

	public int multi(int a, int b) {
		int c=a*b;
		return c;
	}

	public double division(int a,int b) {
		double c=a/b;
		return c;
	}

	public int module(int a, int b) {
		int c=a%b;
		return c;
	}

}
